const I18nCtx = createContext(null);

export function I18nProvider({ children, userLanguage }) {
  const [lang, setLang] = useState(() => {
    const saved = typeof window !== "undefined" && localStorage.getItem("nidhii_lang");
    return saved || userLanguage || "en";
  });

  useEffect(() => {
    if (userLanguage && !localStorage.getItem("nidhii_lang")) setLang(userLanguage);
  }, [userLanguage]);

  const setLanguage = (l) => {
    setLang(l);
    try { localStorage.setItem("nidhii_lang", l); } catch (_) {/*ignore*/}
    if (typeof document !== "undefined") document.documentElement.lang = l;
  };

  const t = (key, vars = {}) => {
    const dict = DICT[lang] || DICT.en;
    let s = dict[key] ?? DICT.en[key] ?? key;
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), v);
    }
    return s;
  };

  return (
    <I18nCtx.Provider value={{ lang, setLanguage, t }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nCtx);
  if (!ctx) throw new Error("useI18n must be inside I18nProvider");
  return ctx;
}
